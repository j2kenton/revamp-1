import { type ReactNode } from 'react';
import type { Metadata } from 'next';
import { Geist, Geist_Mono } from 'next/font/google';
import { ReduxProvider } from '@/lib/redux/ReduxProvider';
import { SessionProvider } from '@/lib/auth/SessionProvider';
import { MsalProvider } from '@/lib/auth/MsalProvider';
import { TanStackQueryProvider } from '@/lib/tanstack-query/provider';
import { WebVitalsReporter } from '@/components/WebVitalsReporter';
import { ThemeProvider } from '@/lib/theme/ThemeProvider';
import './globals.css';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'CR Project App',
  description: 'Generated by ReVamp',
};

const shouldBypassAuth = process.env.BYPASS_AUTH === 'true';
if (shouldBypassAuth) {
  console.info('[Auth] Bypass authentication mode enabled');
}

export default function RootLayout({ children }: { children: ReactNode }) {
  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        {shouldBypassAuth ? (
          <script
            dangerouslySetInnerHTML={{
              __html: 'window.__BYPASS_AUTH__ = true;',
            }}
          />
        ) : null}
        <ThemeProvider>
          <WebVitalsReporter />
          <MsalProvider>
            <SessionProvider>
              <TanStackQueryProvider>
                <ReduxProvider>{children}</ReduxProvider>
              </TanStackQueryProvider>
            </SessionProvider>
          </MsalProvider>
        </ThemeProvider>
      </body>
    </html>
  );
}
